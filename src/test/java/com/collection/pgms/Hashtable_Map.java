package com.collection.pgms;

import java.util.Hashtable;

public class Hashtable_Map {

	public static void main(String[] args) {
		Hashtable<Integer, String> map= new Hashtable<>();
		map.put(2, "hi");
		map.put(3, "heloo");
		map.put(4, "meow");
		map.put(5, "bow");
		map.put(6, "kooo");
		System.out.println(map);
	}

}
